#include <stdio.h>

int main() {
    char ch;
    scanf("%c",&ch);
    ch == 'a' || ch == 'A' ||
    ch == 'e' || ch == 'E' ||
    ch == 'i' || ch == 'I' ||
    ch == 'o' || ch == 'O' ||
    ch == 'u' || ch == 'U' ?
    printf("%c is a vowel",ch) :
    printf("%c is a consonant",ch);
    
    return 0;
}





